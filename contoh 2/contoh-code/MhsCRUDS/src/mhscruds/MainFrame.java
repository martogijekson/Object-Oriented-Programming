    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed

        if (IsInputValid()) {
            if (jtfNIM.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Isi Data NIM");
                return;
            }

            Mahasiswa nMhs = new Mahasiswa();

            nMhs.setNim(jtfNIM.getText());
            nMhs.setNama(jtfNama.getText());
            nMhs.setTglLahir((java.util.Date) jsTglLahir.getValue());
            nMhs.setNoHP(jtfNoHP.getText());
            nMhs.setJnsKelamin(jcbJK.getSelectedItem().toString().substring(0, 1));
            nMhs.setAlamat(jtfAlamat.getText());
            nMhs.setJurusan(jtfJurusan.getText());

            int retValue = -1;

            if (jtfNIM.isEnabled()) //insert data
            {
                retValue = mhsDAL.InsertDataMahasiswa(nMhs);
            } 

            if (retValue > 0) {
                JOptionPane.showMessageDialog(rootPane, "Data Mahasiswa berhasil disimpan");
                ClearForm();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Data Mahasiswa gagal disimpan");
            }
        }
    }//GEN-LAST:event_jbSaveActionPerformed

