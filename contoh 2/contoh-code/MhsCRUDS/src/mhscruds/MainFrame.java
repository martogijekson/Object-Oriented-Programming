    private void SetInputValidator() {
        jtfNIM.setInputVerifier(new InputVerifier() {
            public boolean verify(JComponent comp) {
                boolean returnValue = true;
                JTextField textField = (JTextField) comp;
                String content = textField.getText();
                if (content.length() != 10 && content.length()>0) {
                    returnValue = false;
                } else {
                    if (content.length() != 0) {
                        try {
                            Long.parseLong(textField.getText());
                        } catch (NumberFormatException e) {
                            returnValue = false;
                        }
                    }
                }
                return returnValue;
            }

            public boolean shouldYieldFocus(JComponent input) {
                boolean valid = super.shouldYieldFocus(input);
                if (!valid) {
                    getToolkit().beep();
                    jlWarningNIM.setVisible(true);
                } else {
                    jlWarningNIM.setVisible(false);
                }
                return valid;
            }
        });

        jtfNoHP.setInputVerifier(new InputVerifier() {
            public boolean verify(JComponent comp) {
                boolean returnValue = true;
                JTextField textField = (JTextField) comp;
                String content = textField.getText();
                if (content.length() != 0) {
                    try {
                        Long.parseLong(textField.getText());
                    } catch (NumberFormatException e) {
                        returnValue = false;
                    }
                }
                return returnValue;
            }

            public boolean shouldYieldFocus(JComponent input) {
                boolean valid = super.shouldYieldFocus(input);
                if (!valid) {
                    getToolkit().beep();
                    jlWarningNoHP.setVisible(true);
                } else {
                    jlWarningNoHP.setVisible(false);
                }
                return valid;
            }
        });
    }

